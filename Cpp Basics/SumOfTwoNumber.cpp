/*Write a Program to Add Two Integer Numbers Entered by
User*/
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int num1, num2;
    cout << "ENTER THE VALUE FOR NUMBER-1 : ";
    cin >> num1;
    cout << endl;
    cout << "ENTER THE VALUE FOR NUMBER-2 : ";
    cin >> num2;
    cout << endl;
    cout << "SUM: " << num1 + num2;
    return 0;
}
