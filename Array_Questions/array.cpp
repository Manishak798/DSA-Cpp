/*Write a program in C to store elements in an array and print them.
Test Data :   
Input 10 elements in the array :
element - 0 : 1
element - 1 : 1
element - 2 : 2*/
#include <bits/stdc++.h>
using namespace std;
int main()
{
    int n;
    cout << "enter size of array: ";
    cin >> n;
    int arr[n];
    cout << endl;
    for (int i = 0; i < n; i++)
    {
        cout << "Enter element " << i << " :";
        cin >> arr[i];
        cout << endl;
    }
    cout << "[ ";
    for (auto it : arr)
    {
        cout << it << " ";
    }
    cout << "]";
    return 0;
}
